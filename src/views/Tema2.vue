    p.mb-5(data-aos="fade-right") La teoría de la carga cognitiva de John Sweller establece que el aprendizaje efectivo se logra cuando se reduce la carga cognitiva intrínseca y extrínseca. La primera está relacionada con aspectos de la actividad de aprendizaje mismo, como su complejidad y la experiencia del aprendiz; en cambio, la segunda se asocia con los elementos de diseño de la actividad y elementos contextuales que generan entorpecimiento en los procesos de aprendizaje. Ambas cargas son sumativas y se deben minimizar para permitir un espacio a la carga relevante o germinal, la cual se encarga de los procesos operativos para consolidar el aprendizaje en la memoria a largo plazo. 

